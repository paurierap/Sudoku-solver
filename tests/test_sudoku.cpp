#include <gtest/gtest.h>
#include "Sudoku.hpp"
#include <vector>

using matrix = std::vector<std::vector<int>>;

TEST(Sudoku, ConstructsWithDifficulty) {
    Sudoku s("easy");
    EXPECT_FALSE(s.isSolved());
}

TEST(Sudoku, ConstructorFromBoard)
{
    matrix board = {{5,3,0,0,7,0,0,0,0},
        {6,0,0,1,9,5,0,0,0},
        {0,9,8,0,0,0,0,6,0},
        {8,0,0,0,6,0,0,0,3},
        {4,0,0,8,0,3,0,0,1},
        {7,0,0,0,2,0,0,0,6},
        {0,6,0,0,0,0,2,8,0},
        {0,0,0,4,1,9,0,0,5},
        {0,0,0,0,8,0,0,7,9}
    };
    Sudoku sudoku(board);
    matrix SudokuBoard = sudoku.getBoard();

    EXPECT_EQ(SudokuBoard, board);
}

